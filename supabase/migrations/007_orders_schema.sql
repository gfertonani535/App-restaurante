-- =====================================================
-- CLOSED_AT AUTOMÁTICO
-- =====================================================

create or replace function public.set_order_closed_at()
returns trigger
language plpgsql
set search_path = ''
as $$
begin
  if new.status = 'closed'
    and old.status is distinct from 'closed'
  then
    new.closed_at := now();
  elsif new.status <> 'closed' then
    new.closed_at := null;
  end if;

  return new;
end;
$$;


drop trigger if exists orders_set_closed_at
on public.orders;

create trigger orders_set_closed_at
before update of status
on public.orders
for each row
execute function public.set_order_closed_at();