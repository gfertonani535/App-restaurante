-- =====================================================
-- CALCULAR TOTAL DE LA ORDEN
-- total = subtotal - descuento
-- =====================================================

create or replace function public.compute_order_total()
returns trigger
language plpgsql
set search_path = ''
as $$
begin
  new.total := greatest(
    coalesce(new.subtotal, 0) - coalesce(new.discount, 0),
    0
  );

  return new;
end;
$$;


drop trigger if exists orders_compute_total
on public.orders;

create trigger orders_compute_total
before insert or update of subtotal, discount
on public.orders
for each row
execute function public.compute_order_total();


-- =====================================================
-- ACTUALIZAR ESTADO DE PAGO
-- =====================================================

create or replace function public.refresh_order_payment_status(
  p_order_id uuid
)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_total numeric(12, 2);
  v_paid numeric(12, 2);
  v_payment_status text;
begin
  select total
  into v_total
  from public.orders
  where id = p_order_id;

  if not found then
    return;
  end if;

  select coalesce(sum(amount), 0)
  into v_paid
  from public.payments
  where order_id = p_order_id
    and status = 'completed';

  v_payment_status :=
    case
      when v_total > 0 and v_paid >= v_total then 'paid'
      when v_paid > 0 then 'partial'
      else 'unpaid'
    end;

  update public.orders
  set
    payment_status = v_payment_status,
    paid_at = case
      when v_payment_status = 'paid'
        then coalesce(paid_at, now())
      else null
    end
  where id = p_order_id;
end;
$$;


-- =====================================================
-- RECALCULAR SUBTOTAL DESDE ORDER_ITEMS
-- =====================================================

create or replace function public.refresh_order_totals(
  p_order_id uuid
)
returns void
language plpgsql
security definer
set search_path = ''
as $$
declare
  v_subtotal numeric(12, 2);
begin
  select coalesce(sum(line_total), 0)
  into v_subtotal
  from public.order_items
  where order_id = p_order_id;

  update public.orders
  set subtotal = v_subtotal
  where id = p_order_id;
end;
$$;


create or replace function public.handle_order_items_change()
returns trigger
language plpgsql
security definer
set search_path = ''
as $$
begin
  if tg_op = 'DELETE' then
    perform public.refresh_order_totals(old.order_id);
    return old;
  end if;

  if tg_op = 'UPDATE'
    and old.order_id is distinct from new.order_id
  then
    perform public.refresh_order_totals(old.order_id);
  end if;

  perform public.refresh_order_totals(new.order_id);

  return new;
end;
$$;


drop trigger if exists order_items_refresh_order
on public.order_items;

create trigger order_items_refresh_order
after insert or update or delete
on public.order_items
for each row
execute function public.handle_order_items_change();


